    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedItem().equals("Selectaţi un profesor"))
            return;

        Object selectat = comboModel.getSelectedItem();
        if (comboVect.elementAt(0).equals("Selectaţi un profesor"))
        {
            comboVect.remove(0);
            comboModel = new DefaultComboBoxModel(comboVect);
            comboModel.setSelectedItem(selectat);
            jComboBox1.setModel(comboModel);
        }
        
        int index = 0, indexSelectat = jComboBox1.getSelectedIndex();
        for (Iterator<Profesor> i = Centralizator.getInstance().
                iteratorProfesori(); i.hasNext(); ++index)
        {
            Profesor profesorCurent = i.next();
            if (index == indexSelectat)
            {
                profesorSelectat = profesorCurent;
                break;
            }
        }
        panouMateriiButoane.activeaza(profesorSelectat);
        panouChecks.activeaza();
    }//GEN-LAST:event_jComboBox1ActionPerformed

