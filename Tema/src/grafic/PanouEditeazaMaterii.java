    public PanouEditeazaMaterii()
    {
        initComponents();
        comboVect = new Vector<String>();
        
        ArrayList<Profesor> profi;
        for (Iterator<Profesor> i = Centralizator.getInstance().
                iteratorProfesori(); i.hasNext(); )
        {
            Profesor prof = i.next();
            comboVect.add(prof.getPrenume() + " " + prof.getNume());
        }
        comboVect.insertElementAt("Selectaţi un profesor", 0);
        
        comboModel = new DefaultComboBoxModel(comboVect);
        jComboBox1.setModel(comboModel);
        
        toateClasele = new ArrayList<Clasa>();
        Centralizator centralizator = Centralizator.getInstance();
        for (Iterator<Clasa> i = centralizator.getClase(null).iterator();
                i.hasNext(); )
            toateClasele.add(i.next());
        
        jPanel1.setLayout(new GridLayout(1, 2, 5, 0));
        panouMateriiButoane = new PanouListaMaterii();
        jPanel1.add(panouMateriiButoane);
        panouChecks = new PanouCheckBoxes();
        jPanel1.add(panouChecks);
    }
    
    public Iterator<Clasa> iteratorClase()
    {
        return toateClasele.iterator();
    }
    
